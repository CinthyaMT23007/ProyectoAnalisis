class Paciente:
    def __init__(self, cod_paciente, nombre, apellido, edad, dui, direccion, telefono, id_rol, cod_expediente):
        self.cod_paciente = cod_paciente
        self.nombre = nombre
        self.apellido = apellido
        self.edad = edad
        self.dui = dui
        self.direccion = direccion
        self.telefono = telefono
        self.id_rol = id_rol
        self.cod_expediente = cod_expediente
